/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_CAN_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  MessagingCfg_Typedef messaging_cfg = { 0 };
  int32_t can_msg_per[CAN_MESSAGING_TOTAL_MESSAGES] = { 0U };
  uint32_t can_msg_time[CAN_MESSAGING_TOTAL_MESSAGES] = { 0U };

  ReadingTypedef reading_struct[READINGS_NUMBER_OF_CHANNELS] = { 0 };
  uint8_t verify_buffer[10] = { 0U };
  uint32_t reading_buffer[10] = { 0U };

  uint16_t filter_freq_buffer[READINGS_NUMBER_OF_CHANNELS] = { 0U };
  uint8_t filter_q_factor_buffer[READINGS_NUMBER_OF_CHANNELS] = { 0U };

  assert(BSP_EEPROM_Init(0U) >= BSP_ERROR_NONE);

  assert(mem_load_can_cfg(&messaging_cfg) >= 0);
  assert(mem_load_can_msg_freqs(&can_msg_per[0], CAN_MESSAGING_DATA_MESSAGES) >= 0);
  assert(mem_load_filters(&filter_freq_buffer[0], &filter_q_factor_buffer[0], READINGS_NUMBER_OF_CHANNELS) >= 0);

  assert(BSP_TEMPERATURE_Init() == BSP_ERROR_NONE);

  for (uint8_t i = 0U; i < READINGS_NUMBER_OF_CHANNELS; ++i) {
    assert(readings_init(&reading_struct[i], i, &reading_buffer[i], &verify_buffer[i]) >= 0);
    assert(readings_set_filter(&reading_struct[i], &filter_freq_buffer[i], &filter_q_factor_buffer[i]) >= 0);
  }

 assert(can_messaging_init(&messaging_cfg) >= 0);

  for (uint8_t i = kLed1; i < kLedAmount; ++i) {
  	assert(BSP_LED_Init(i) == BSP_ERROR_NONE);
  }

  // Set initial time equal to current tick time
  for (uint8_t i = 0U; i < CAN_MESSAGING_TOTAL_MESSAGES; ++i) {
    can_msg_time[i] = BSP_GetTimeMs();
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    for (uint8_t i = 0U; i < READINGS_NUMBER_OF_CHANNELS; ++i) {
		  readings_calc(&reading_struct[i]);
    }

  	if (can_rx_callback == 1U) {
  		can_rx_callback = 0U;
  	}

  	for (uint8_t i = 0U; i < CAN_MESSAGING_TOTAL_MESSAGES; ++i) {
  		if (((BSP_GetTimeMs() - can_msg_time[i]) >= can_msg_per[i])
  		      && can_msg_per[i] != CAN_MESSAGING_MESSAGE_DISABLED) {
  		  // Increment period to keep average interval the same as the period
  			can_msg_time[i] += can_msg_per[i];
  			can_messaging_send(i, &reading_buffer[0], &verify_buffer[0]);
  		}
  	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

